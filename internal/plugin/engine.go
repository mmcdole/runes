func (pe *PluginEngine) Start() {
	// go func() {
	pe.logger.Debug("[PluginEngine]: Started")

	err := pe.loadOrCreateDefaultPlugin()
	if err != nil {
		pe.logger.Error("Failed to create or load default plugin: %v", err)
		return
	}

	go pe.processCommands()
	go pe.processText()
}

func (pe *PluginEngine) EnqueueCommand(cmd string) {
	pe.inCommandChan <- cmd
}

func (pe *PluginEngine) EnqueueText(text string) {
	pe.inTextLineChan <- text
}

func (pe *PluginEngine) ReceiveCommands() chan string {
	return pe.outCmdChan
}

func (pe *PluginEngine) ReceiveProcessedCommands() chan string {
	return pe.outProcessedCmdChan
}

func (pe *PluginEngine) ReceiveTextLines() chan types.BufferOutput {
	return pe.outTextLineChan
}

func (pe *PluginEngine) processCommands() {
	for cmd := range pe.inCommandChan {
		pe.handleCommand(cmd)
	}
}

func (pe *PluginEngine) processText() {
	for line := range pe.inTextLineChan {
		pe.handleTextLine(line)
	}
}

func (pe *PluginEngine) loadOrCreateDefaultPlugin() error {
	defaultPath := filepath.Join(pe.config.ConfigDir, "init.lua")

	// Check if default plugin exists and create empty one if it doesn't
	if _, err := os.Stat(defaultPath); os.IsNotExist(err) {
		file, err := os.Create(defaultPath)
		if err != nil {
			return err
		}
		defer file.Close()
	}

	// Load the default plugin
	defaultPlugin := NewPlugin("default", defaultPath, pe)
	pe.plugins = append(pe.plugins, defaultPlugin)
	err := defaultPlugin.Load()
	if err != nil {
		return err
	}

	return nil
}

func (pe *PluginEngine) handleCommand(command string) {
	// TODO: process commands and check against alias list
	pe.logger.Trace("[PluginEngine]: Command In: %s", strings.TrimSpace(command))

	// TODO: Process Commands/check for aliases
	handledAlias := false
	for _, plugin := range pe.plugins {
		if executed := plugin.CheckAndExecuteAlias(command); executed {
			handledAlias = handledAlias || executed
		}
	}

	if handledAlias {
		pe.logger.Trace("[PluginEngine]: Command Matched Alias: %s", strings.TrimSpace(command))
	} else {
		pe.logger.Trace("[PluginEngine]: Command Out (Passthrough): %s", strings.TrimSpace(command))
		pe.outProcessedCmdChan <- command
	}
}

